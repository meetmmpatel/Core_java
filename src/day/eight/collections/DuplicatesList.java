package day.eight.collections;

import java.util.ArrayList;

public class DuplicatesList {
	
	public static void main(String[] args) {
		
		ArrayList<Integer> list1 = new ArrayList<>();
		ArrayList<Integer> list2 = new ArrayList<>();
		
		list1.add(1);
		list1.add(2);
		list1.add(3);
		list1.add(4);
		list1.add(5);
		list1.add(6);
		
		list2.add(5);
		list2.add(6);
		list2.add(8);
		list2.add(9);
		
	}

}
